package com.upsoft.sep.sls.config.check.param;

import io.swagger.annotations.ApiModelProperty;

public class MoveOptParam {
	@ApiModelProperty(value = "选中选项目ID", required = true)
	private String optId;
	private Integer orderNumber;
	@ApiModelProperty(value = "步进", required = true)
	private Integer increment;
	private String optRemovedId;

	public String getOptId() {
		return optId;
	}

	public void setOptId(String optId) {
		this.optId = optId;
	}

	public Integer getOrderNumber() {
		return orderNumber;
	}

	public void setOrderNumber(Integer orderNumber) {
		this.orderNumber = orderNumber;
	}

	public Integer getIncrement() {
		return increment;
	}

	public void setIncrement(Integer increment) {
		this.increment = increment;
	}

	public String getOptRemovedId() {
		return optRemovedId;
	}

	public void setOptRemovedId(String optRemovedId) {
		this.optRemovedId = optRemovedId;
	}
	
}
